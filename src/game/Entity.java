
package game;

/**
 * Basic Entity Class  
 * @author Nathan Geddis
 */
public class Entity {
    private int xPOS;
    private int yPOS;
    private String name;

    public int getXPOS() {
        return xPOS;
    }

    public void setXPOS(int xPOS) {
        this.xPOS = xPOS;
    }

    public int getYPOS() {
        return yPOS;
    }

    public void setYPOS(int yPOS) {
        this.yPOS = yPOS;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
